import useToken from './useToken'

function PrivateRoute({ children, ...rest }) {
    const { token, setToken } = useToken();
    return (
        <Route
            {...rest}
            render={({ location }) =>
                token ? (
                    children
                ) : (
                    <Redirect
                        to={{
                            pathname: "/login",
                            state: { from: location }
                        }}
                    />
                )
            }
        />
    );
}
